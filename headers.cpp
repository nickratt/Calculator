#include <iostream>
#include <cstdlib>
#include "headers.h"

using namespace std;

int addition(int a, int b)
{
int sum;

sum = a + b;
return sum;

}

int subtraction(int a, int b)
{
  int diff;

  diff = a - b;
  return diff;

}

int multiplication(int a, int b)
{
  int mult;
  mult = a * b;
  return mult;
  
}

int division(int a, int b)
{
  int div;
  div = a / b;
  return div;
  
}
